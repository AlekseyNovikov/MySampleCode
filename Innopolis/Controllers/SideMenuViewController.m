#import "SWRevealViewController.h"
#import "WebViewController.h"

@interface SideMenuViewController ()
@property (weak, nonatomic) IBOutlet UIView *headerMenuView;

@end

@implementation SideMenuViewController{
    NSArray *menuItems;
}

- (void)viewDidLoad {
    [super viewDidLoad];
    
    menuItems = @[@"NewsCell", @"AboutCell", @"TimetableCell", @"SectionsCell", @"SpeakersCell", @"GuestsCell", @"PartnersCell", @"MapCell"];
    
    _headerMenuView.frame = CGRectMake(0, 0, self.tableView.frame.size.width, 150.f);
    self.tableView.tableFooterView = [UIView new];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    // Return the number of rows in the section.
    return menuItems.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {    
    
    NSString *CellIdentifier = [menuItems objectAtIndex:indexPath.row];
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    return cell;
}


- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender {
    
    // Set the title of navigation bar by using the menu items
    NSIndexPath *indexPath = [self.tableView indexPathForSelectedRow];
    UINavigationController *destViewController = (UINavigationController*)segue.destinationViewController;
    destViewController.title = [[menuItems objectAtIndex:indexPath.row] capitalizedString];
    
    // Set the photo if it navigates to the PhotoView
    if ([segue.identifier isEqualToString:@"showWebView"]) {
        UINavigationController *navController = segue.destinationViewController;
        WebViewController *photoController = [navController childViewControllers].firstObject;
        photoController.title = [menuItems objectAtIndex:indexPath.row];
        NSString *photoFilename = [NSString stringWithFormat:@"%@_photo", [menuItems objectAtIndex:indexPath.row]];
//        WebViewController.photoFilename = photoFilename;
    }
}

